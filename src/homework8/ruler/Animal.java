package homework8.ruler;

public class Animal {
    private int bodyLenght;
    private int tailLenght;

    public Animal(int bodyLenght, int tailLenght) {
        this.bodyLenght = bodyLenght;
        this.tailLenght = tailLenght;
    }

    public int getBodyLenght() {
        return bodyLenght;
    }

    public int getTailLenght() {
        return tailLenght;
    }
}
